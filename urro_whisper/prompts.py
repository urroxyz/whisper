def SPEAKERS(n, lang='en', ch="[", short=False):
    """
    Generate a single-line string of unique speaker utterances in random order
    for a specified number of speakers and language.

    Parameters:
        n (int): Number of speakers (1 through 20).
        lang (str): The language code (e.g., 'en', 'zh', 'de').
                         Must be a valid key in templates.

    Returns:
        str: Space-separated utterances labeled with speaker numbers, no line breaks.

    Raises:
        ValueError: If n is not between 1 and len(templates['en'])
                    or if lang is not supported.
    """
    max_speakers = len(templates['en']) # All lists should have 20 items

    if not (1 <= n <= max_speakers):
        raise ValueError(f"n must be between 1 and {max_speakers}")

    if lang not in templates:
        raise ValueError(f"Language code '{lang}' is not supported. Supported codes are: {', '.join(templates.keys())}")

    lang_templates = templates[lang]

    mirror = MIRRORED.get(ch, ch)

    label = "SPEAKER " if not short else "S"

    # Prepare utterances for speakers 1 to n
    utterances = []
    for i in range(1, n + 1):
        # Use i-1 because list indices are 0-based
        template = lang_templates[i - 1]
        text = template.format(delimiter="Speaker", num=i)
        utterances.append(f"{ch}{label}{i}{mirror} {text}")

    # Randomize order
    random.shuffle(utterances)

    # Concatenate into a single-line string
    return " ".join(utterances)

